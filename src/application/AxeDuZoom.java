
package application;


public enum AxeDuZoom {
	/**
	 * 	 permet d'effectuer le zoom sur l'axe horizontal (X)
	 */
	Horizontal,

	/**
	 * permet d'effectuer le zoom sur l'axe vertical (Y)
	 */
	Vertical,

	/**
	 * permet d'effectuer le zoom sur les deux axe vertical+horizontal ( donc sur la zone de tracage du graphe)
	 */
	HorVer,

	/**
	 * aucun zoom n'est permis
	 */
	None
}